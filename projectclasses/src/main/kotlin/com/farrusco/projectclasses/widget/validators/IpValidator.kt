package com.farrusco.projectclasses.widget.validators

import java.util.regex.Pattern

class IpValidator(error: String) : RegExpValidator(CNDITIONEXP, error) {

    companion object {
        private const val IP_ADDRESS_STRING =
        "((25[0-5]|2[0-4][0-9]|[0-1][0-9]{2}|[1-9][0-9]|[1-9])\\.(25[0-5]|2[0-4]" +
        "[0-9]|[0-1][0-9]{2}|[1-9][0-9]|[1-9]|0)\\.(25[0-5]|2[0-4][0-9]|[0-1]" +
        "[0-9]{2}|[1-9][0-9]|[1-9]|0)\\.(25[0-5]|2[0-4][0-9]|[0-1][0-9]{2}" +
        "|[1-9][0-9]|[0-9]))"
        var CNDITIONEXP: Pattern = Pattern.compile(IP_ADDRESS_STRING)
    }
}